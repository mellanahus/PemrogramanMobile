fun main() {

    // Ini hanya komentar
    println("Hello Ayang")
    println("Apa Kabar ?")
    print("Cek")
    println("=============")
    println(200)
    println(3 * 5)
    println("=============")
    println()

    // Ini contoh menggunakan tipe data Integer
    var umur: Byte = 20
    var tinggi: Int = 160

    println("Saya sekarang berusia " + umur + " Tahun dengan tinggi badan " + tinggi + " cm")

    // Ini contoh menggunakan tipe data float
    var value: Float = 3.9f

    println("Nilai IPK saya " + value)

    // Ini contoh literal
    var binner: Int = 0b010101010
    println(binner)
}